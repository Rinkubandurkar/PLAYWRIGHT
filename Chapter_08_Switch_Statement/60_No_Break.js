let day = 2;
switch (day) {
    case 0:
        console.log("Monday");
    case 1:
        console.log("Tuesday");
    case 2:
        console.log("Wednesday");
    case 3:
        console.log("Thursday");
    case 4:
        console.log("Friday");
    case 5:
        console.log("Saturday");
    case 6:
        console.log("Sunday");
    default:
        console.log("Invalid day number! Please enter a number between 0 and 6.");
}