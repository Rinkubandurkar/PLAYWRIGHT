// Comparison Operators

//< ,>,>=, <=, ==, ===, !=, !==
// = is an assignment operator, it assigns a value to a variable
// == is a comparison operator, it compares two values and returns true or false
// === is a strict comparison operator, it compares two values and returns true or false, it also checks the type of the values
// != is a comparison operator, it compares two values and returns true or false, it returns true if the values are not equal
// !== is a strict comparison operator, it compares two values and returns true or false, it also checks the type of the values, it returns true if the values are not equal    

console.log(3 > 4);
console.log(3 < 4);
console.log(3 >= 4);
console.log(3 <= 4);
console.log(3 == 4);
console.log(4 === 4);
console.log(3 != 4);
console.log(3 !== 4);
